function displayCoursesByLevel(level, containerSelector) {
    const injectExtraCourses = document.querySelector(containerSelector);

    const filteredCourses = allExtraCourses.filter(course => course.Level === level);

    const extraCoursesHTML = filteredCourses.map(course => {
        return `
            <div class="course-grid">
                <img src="${course.Img}" alt="${course.Name}">
                <div class="middle-align">
                    <h3>${course.Name}</h3>
                    <p>${course.Type}</p>
                    <p>${course.Description}</p>
                </div>
                <div class="buttons-course middle-align">
                    <div class="buttons-course-upper">
                        <a class="button red" target="_blank" href="${course.BuyNowLink}">BUY NOW</a>
                        <div class="dropdown button red">
                            <button class="paymentplan">PAYMENT PLAN</button>
                            <div class="dropdown-content">
                                <a href="${course.ThreeMonthLink}" target="blank">3 Months</a>
                                <a href="${course.SixMonthLink}" target="blank">6 Months</a>
                            </div>
                        </div> 
                    </div>
                        <a class="button red" href="${course.CourseGuideLink}">DOWNLOAD COURSE GUIDE</a>
                </div>
            </div>
        `;
    }).join('');

    injectExtraCourses.innerHTML = extraCoursesHTML;
}

